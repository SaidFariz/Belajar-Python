# Mengambil input data

print("=== String ===")
satu = input ("siapa namanya ")
print(satu, "variable = ", type(satu))


print("=== Integer ===")
angka = int(input("masukkan angka = "))
print(angka, "variable =", type(angka))


print("=== Float ===")
number = float(input ("masukkan ongko = "))
print(number, "variable = ", type(number))


print("=== Boolean ===")
boolean = bool(int(input("masukkan nilai = ")))
print(boolean, "variable = ", type(boolean))