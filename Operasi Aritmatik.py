# melakukan operasi aritmatika
'''operasi aritmatik
(penjumlahan, pengurangan, perkalian pembagian, modulus, pemangkatan, pembagian bulat)'''

print("=== Penjumlahan (+) ===")
a = 1
b = 4
print(a + b)
'''
# bisa juga dengan 
a = 1
b = 4
hasil = a + b # tergantung operasinya (+ - * / % ** //)
print(hasil)
'''

print("=== Pengurangan (-) ===")
c = 7
d = 6
print(c - d)

print("=== Perkalian (*) ===")
e = 8
f = 10
print(e * f)

print("=== Pembagian (/) ===")
g = 8
h = 2
print(g / h)

print("=== Modulus (%) ===")
i = 9
j = 5
print(i % j) # Hasilnya adalah sisa bagi dari operasi pembagian i dan j

print("=== Pemangkatan (**) ===")
k = 9
l = 2
print(k ** l) # Nilai k akan dipangkatkan nilai l

print("=== Pembagian Bulat (//) ===")
m = 7
n = 4
print(m // n) # Hasil akan dibulatkan kebawah
